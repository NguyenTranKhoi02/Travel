// Helper to get current language translation
function t(key) {
  const lang = localStorage.getItem('lang') || 'vi';
  return (translations[lang] && translations[lang][key]) || (translations.vi[key]) || key;
}

function changeLanguage(lang) {
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang;

  // Update elements with data-i18n attribute
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      const value = translations[lang][key];
      // If value contains HTML tags, use innerHTML
      if (value.includes('<')) {
        el.innerHTML = value;
      } else {
        if (el.tagName === 'INPUT' && (el.type === 'button' || el.type === 'submit')) {
          el.value = value;
        } else {
          el.textContent = value;
        }
      }
    }
  });

  // Update elements with data-i18n-placeholder attribute
  const placeholderEls = document.querySelectorAll('[data-i18n-placeholder]');
  placeholderEls.forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang] && translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });

  // Update active state of language switcher buttons
  const langBtns = document.querySelectorAll('.lang-btn');
  langBtns.forEach(btn => {
    if (btn.dataset.lang === lang) {
      btn.classList.add('active');
    } else {
      btn.classList.remove('active');
    }
  });
}

document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('lang') || 'vi';
  changeLanguage(savedLang);

  // Bind language buttons
  const langBtns = document.querySelectorAll('.lang-btn');
  langBtns.forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      changeLanguage(btn.dataset.lang);
    });
  });
});
